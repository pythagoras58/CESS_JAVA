package DayTwo;

public class SwitchCases {
    public static void main(String[] args) {
        int number = 20;

        switch (number){
            case 1:
                System.out.println("January");
                break;
            case 2:
                System.out.println("February");
                break;
            default:
                System.out.println("December");
                break;
        }
    }
}
